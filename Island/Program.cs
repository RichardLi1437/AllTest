        static void PrintIslands(int[,] islands, int m, int n) 
        {
            for (int i = 0; i < m; i++)
            {
                for (int j = 0; j < n; j++)
                {
                    if (islands[i, j] < 0) System.Console.Write("I");
                    else System.Console.Write(islands[i, j]);
                }
                System.Console.WriteLine();
            }
        }

        static void MarkIslands(int[,] islands, int m, int n)
        {
            int idx = 0;
            for (int i = 0; i < m; i++)
            {
                for (int j = 0; j < n; j++)
                {
                    if (islands[i, j] >= 0) continue;
                    MarkIsland(islands, m, n, i, j, ++idx);
                }
            }
        }

        static void MarkIsland(int[,] islands, int m, int n, int iRow, int iCol, int idx)
        {
            Queue<Pos> q = new Queue<Pos>();
            q.Enqueue(new Pos(iRow, iCol));
            while (q.Count<Pos>() > 0) 
            {
                Pos pos = q.Dequeue();
                islands[pos.row, pos.col] = idx;
                for (int i = -1; i <= 1; i++)
                {
                    for (int j = -1; j <= 1; j++)
                    {
                        if (i == 0 && j == 0) continue;
                        Pos pp = new Pos(pos.row+i, pos.col + j);
                        if (Island(pp, islands, m, n))
                        {
                            q.Enqueue(pp);
                        }
                    }
                }
            }
        }

        static bool Island(Pos p, int[,] islands, int rowC, int colC)
        {
            return (p.row >= 0 && p.row < rowC && p.col >= 0 && p.col < colC && islands[p.row, p.col] < 0);
        }
